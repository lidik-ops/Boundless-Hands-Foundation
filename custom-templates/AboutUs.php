    <p>
    We raise funds through donations from partners, various companies and compassionate individuals. To the selected schools (especially specialized schools for CWDs), we liaise with the head teacher who identifies the needs of these children that cannot be provided by the school or parents. Case in point is Ngora Primary School for the Deaf. The head teacher says most of his pupils, who are considered an abomination to their respective families, are dumped to the school by parents. Consequently, such children are left to stay at the school and under the mercy of the teachers whose earnings are also meagre. Upon realising this, we formed and registered an organisation that can solicit for assistance to such children.
Our long-tern plan is to equip these children with skills that can give them sustainable livelihoods even after attaining formal education. We work in partnership with Uganda National Association for the Deaf, Hillcrest Safaris, Land Rover Uganda Club, Fun Factory, SoSure Pads, Banyakitara in Mbale, Gibbs Wine and Liquor Store, BINS, Siko Photography, Nation Media Pictures, and many various individuals.
